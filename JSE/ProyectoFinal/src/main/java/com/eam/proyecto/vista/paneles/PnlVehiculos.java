    private void btnGuardarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarVehiculoActionPerformed
        if (txtPlacaVehiculo.getText().isEmpty() || txtPlacaVehiculo.getText().equalsIgnoreCase("Placa vehiculo")
                || txtModeloVehiculo.getText().equalsIgnoreCase("Modelo del vehiculo") || txtModeloVehiculo.getText().isEmpty()
                || txtLineaVehiculo.getText().equalsIgnoreCase("Linea del vehiculo") || txtLineaVehiculo.getText().isEmpty()
                || txtMarcaVehiculo.getText().equalsIgnoreCase("Marca del vehiculo") || txtMarcaVehiculo.getText().isEmpty()
                || txtLicenciaTransito.getText().equalsIgnoreCase("Licencia de transito") || txtLicenciaTransito.getText().isEmpty()
                || txtNacionalidad.getText().equalsIgnoreCase("Nacionalidad del vehiculo") || txtNacionalidad.getText().isEmpty()
                || txtNacionalidad.getText().equalsIgnoreCase("Nacionalidad del vehiculo") || txtNacionalidad.getText().isEmpty()
                || txtColorVehiculo.getText().equalsIgnoreCase("Color del vehiculo") || txtColorVehiculo.getText().isEmpty()
                || txtLugarMatricula.getText().equalsIgnoreCase("Lugar matricula") || txtColorVehiculo.getText().isEmpty()
                || txtPropietario.getText().equalsIgnoreCase("Propietario del vehiculo") || txtPropietario.getText().isEmpty()
                || spCarga.getValue().toString().equalsIgnoreCase("0") || spPasajeros.getValue().toString().equalsIgnoreCase("0")
                || cbClaseVehiculo.getSelectedIndex() == 0 || cbTipovehiculo.getSelectedIndex() == 0
                || txtFechaVehiculo == null) {
            JOptionPane.showMessageDialog(this, "Por favor llene los campos obligatorios");
        } else {
            if (lstPropietario.getSelectedIndex() < 0) {
                JOptionPane.showMessageDialog(this, "Por favor seleccione un propietario de la lista");
                return;
            }

            String palca, modelo, linea, marca, licencia, nacionalidad, color, tipo, clase, propietario, empresa = null, targeta, lugar;
            int pasajeros, carga;
            Date fecha = null;

            palca = txtPlacaVehiculo.getText().trim().trim();
            modelo = txtModeloVehiculo.getText().trim().trim();
            linea = txtLineaVehiculo.getText().trim().trim();
            marca = txtMarcaVehiculo.getText().trim().trim();
            licencia = txtLicenciaTransito.getText().trim().trim();
            nacionalidad = txtNacionalidad.getText().trim().trim();
            color = txtColorVehiculo.getText().trim().trim();
            tipo = cbTipovehiculo.getSelectedItem() + "";
            clase = cbClaseVehiculo.getSelectedItem() + "";
            String[] arrayPropietario = txtPropietario.getText().trim().split("-");
            propietario = arrayPropietario[0];
            lugar = txtLugarMatricula.getText().trim().trim();
            pasajeros = Integer.parseInt(spPasajeros.getValue() + "");
            carga = Integer.parseInt(spCarga.getValue() + "");
            String[] arrayEmpresa = txtEmpresa.getText().trim().split("-");
            empresa = arrayEmpresa[0];
            targeta = txtOperacion.getText().trim().trim();
            if (txtFechaVehiculo.getDate() != null) {
                fecha = txtFechaVehiculo.getDate();
            }

            if (txtEmpresa.getText().isEmpty() || txtEmpresa.getText().equalsIgnoreCase("Empresa del vehiculo")) {
                if (controVehiculo.guardar(palca, modelo, linea, marca, licencia, clase, tipo, lugar, nacionalidad, color, null, carga, pasajeros, 0)) {
                    if (controVehiculo.guardarDueño("", palca, propietario, fecha)) {
                        JOptionPane.showMessageDialog(this, "Registro vehiculo exitoso");
                    } else {
                        JOptionPane.showMessageDialog(this, "Hubo un error");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "La placa ya existe");
                }
            } else {
                if (txtOperacion.getText().isEmpty() || txtOperacion.getText().equalsIgnoreCase("Targeta operacion")) {
                    JOptionPane.showMessageDialog(this, "Por favor llene la targeta de operacion");

                } else {

                    if ((!(txtEmpresa.getText().isEmpty())) && lstEmpresas.getSelectedIndex() < 0) {
                        JOptionPane.showMessageDialog(this, "Si desea agregar una empresa, seleccione uno de la lista");
                        return;
                    } else if (controVehiculo.guardar(palca, modelo, linea, marca, licencia, clase, tipo, lugar, nacionalidad, color, empresa, carga, pasajeros, Integer.parseInt(targeta))) {
                        if (controVehiculo.guardarDueño("", palca, propietario, fecha)) {
                            JOptionPane.showMessageDialog(this, "Registro vehiculo exitoso");
                        } else {
                            JOptionPane.showMessageDialog(this, "Hubo un error");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "La placa ya existe");
                    }

                }
            }

        }

        this.listaVehiculo();
    }//GEN-LAST:event_btnGuardarVehiculoActionPerformed

